 //Identify the data type of each of the following values in Dart: - 3.14 - "Hello" - true 
//42


// The answer
/*
3.14	     double 
"Hello"  	String 
true	     bool 
42	       int 
*/

void main() {
  double pi = 3.14;             //double
  String text = "Hello";        //String
  bool isTrue = true;           //bool
  int number = 42;              //int

  print(pi);         
  print(text); 
  print(isTrue);     
  print(number);     
}
